// One-off: build src/lib/utils/mock-images.ts from DummyJSON's catalog.
// Real product thumbnails on a reliable CDN (cdn.dummyjson.com), grouped into
// pools that roughly match our app categories. Replaces the flaky picsum.photos
// placeholders. Run: node scripts/build-mock-images.cjs  (needs network).
const fs = require('fs');

// app category -> DummyJSON categories
const MAP = {
  electronics: ['smartphones', 'laptops', 'tablets', 'mobile-accessories'],
  fashion: ['mens-shirts', 'womens-dresses', 'tops', 'mens-shoes', 'womens-shoes', 'womens-bags', 'sunglasses', 'mens-watches', 'womens-watches', 'womens-jewellery'],
  sports: ['sports-accessories', 'mens-shoes', 'womens-shoes'],
  beauty: ['beauty', 'skin-care', 'fragrances'],
  automotive: ['vehicle', 'motorcycle'],
  // toys, books, travel: no good match -> fallback pool
};
const CAP = 16, FALLBACK_CAP = 28;

(async () => {
  const res = await fetch('https://dummyjson.com/products?limit=200&select=category,thumbnail');
  const data = await res.json();
  const byCat = {};
  for (const p of data.products) (byCat[p.category] = byCat[p.category] || []).push(p.thumbnail);

  const pools = {};
  for (const [app, djCats] of Object.entries(MAP)) {
    const urls = [];
    for (const c of djCats) if (byCat[c]) urls.push(...byCat[c]);
    pools[app] = urls.slice(0, CAP);
  }
  // diverse fallback: round-robin one from each DJ category until full
  const cats = Object.keys(byCat);
  const fallback = [];
  for (let i = 0; fallback.length < FALLBACK_CAP; i++) {
    let added = false;
    for (const c of cats) { if (byCat[c][i]) { fallback.push(byCat[c][i]); added = true; if (fallback.length >= FALLBACK_CAP) break; } }
    if (!added) break;
  }

  const out = `// AUTO-GENERATED by scripts/build-mock-images.cjs — do not edit by hand.
// Real product thumbnails from DummyJSON's CDN, grouped to roughly match our
// categories. Placeholder/mock imagery; replace with a real feed when available.
/* eslint-disable */

export const MOCK_IMAGES_BY_CATEGORY: Record<string, string[]> = ${JSON.stringify(pools, null, 2)};

export const MOCK_IMAGES_FALLBACK: string[] = ${JSON.stringify(fallback, null, 2)};

function hash(seed: string): number {
  let h = 2166136261;
  for (let i = 0; i < seed.length; i++) { h ^= seed.charCodeAt(i); h = Math.imul(h, 16777619); }
  return h >>> 0;
}

function pool(category?: string): string[] {
  const p = category ? MOCK_IMAGES_BY_CATEGORY[category] : undefined;
  return p && p.length ? p : MOCK_IMAGES_FALLBACK;
}

/** Deterministic placeholder product image for a seed (stable per product). */
export function mockImage(seed: string, category?: string): string {
  const p = pool(category);
  return p[hash(seed) % p.length];
}

/** \`n\` deterministic images from the category pool (for the modal gallery). */
export function mockImages(seed: string, n: number, category?: string): string[] {
  const p = pool(category);
  const start = hash(seed) % p.length;
  return Array.from({ length: n }, (_, k) => p[(start + k) % p.length]);
}
`;
  fs.writeFileSync('src/lib/utils/mock-images.ts', out);
  const counts = Object.fromEntries(Object.entries(pools).map(([k, v]) => [k, v.length]));
  console.log(JSON.stringify({ categories: counts, fallback: fallback.length }));
})();
